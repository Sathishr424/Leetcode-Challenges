from test import Test
       
def switch_gravity_on(lst):
    ok = True
    for i in range(len(lst)-1):
        for j in range(len(lst[i])):
            if lst[i][j] == '#' and lst[i+1][j] == '-':
                lst[i+1][j], lst[i][j] = lst[i][j], lst[i+1][j]
                ok = False
    if not ok:
        return switch_gravity_on(lst)
    return lst
        

Test.assert_equals(switch_gravity_on([
    ["-", "#", "#", "-"],
    ["-", "-", "-", "-"],
    ["-", "-", "-", "-"],
    ["-", "-", "-", "-"]
]
), [
    ["-", "-", "-", "-"],
    ["-", "-", "-", "-"],
    ["-", "-", "-", "-"],
    ["-", "#", "#", "-"]
])


Test.assert_equals(switch_gravity_on([
    ["-", "#", "#", "-"],
    ["-", "-", "#", "-"],
    ["-", "-", "-", "-"],
]
), [
    ["-", "-", "-", "-"],
    ["-", "-", "#", "-"],
    ["-", "#", "#", "-"]
])


